numberone = int(input("Enter first number: "))
numbertwo = int(input("Enter second number: "))

if numberone < numbertwo:
	print(numberone, "is less than ", numbertwo)
else:
	print(numberone, "is not less than ", numbertwo)


if numberone > numbertwo:
	print(numberone, "is greater than ", numbertwo)
else:
	print(numberone, "is not greater than ", numbertwo)


if numberone == numbertwo:
	print(numberone, "is equal to ", numbertwo)
else:
	print(numberone, "is not equal to ", numbertwo)

